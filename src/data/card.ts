import type { CardYear, PatternTile, TileGroup } from '../types/hand';

// Helper functions for building hand patterns concisely
const s = (suitVar: 'X' | 'Y' | 'Z', rank: 1|2|3|4|5|6|7|8|9): PatternTile =>
  ({ kind: 'suited', suitVar, rank });

const w = (wind: 'N' | 'E' | 'W' | 'S' | 'any'): PatternTile =>
  ({ kind: 'wind', wind });

const d = (dragon: 'red' | 'green' | 'white' | 'matchSuit'): PatternTile =>
  ({ kind: 'dragon', dragon });

const fl: PatternTile = { kind: 'flower' };

const exposed = (tiles: PatternTile[]): TileGroup => ({ tiles, exposure: 'exposed' });
const concealed = (tiles: PatternTile[]): TileGroup => ({ tiles, exposure: 'concealed' });

